/**
 * @param {number[]} list
 * @return {number[]}
 * @description Given a list of numbers, reverse the order of the list using the reverse method.
 * @example
 * Input: [1, 2, 3, 4, 5]
 * Output: [5, 4, 3, 2, 1]
 */
export function reverseList(list: number[]): number[] {
    return list.reverse()
}
